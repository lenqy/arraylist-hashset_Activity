public class Laptop {
    public String color;
    public String TypeofLaptop;

    public Laptop(String TypeofLaptop, String color){
        this.TypeofLaptop = TypeofLaptop;
        this.color = color;
    }
    public String getColor() {
        return color;
    }
    public String getTypeofLaptop() {
        return TypeofLaptop;
    }


}